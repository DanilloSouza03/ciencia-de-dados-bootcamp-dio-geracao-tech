
saldo = 450
saque = 200

#Igualdade
print(f"{saldo} é igual a {saque}:", saldo == saque)

#Diferente
print(f"{saldo} é diferente de {saque}:", saldo != saque)

#Maior
print(f"{saldo} é maior que {saque}:", saldo > saque)

#Maior ou Igual
print(f"{saldo} é maior ou igual que {saque}:", saldo >= saque)

#Menor
print(f"{saldo} é menor que {saque}:", saldo < saque)

#Menor ou Igual
print(f"{saldo} é menor ou igual que {saque}:", saldo <= saque)
